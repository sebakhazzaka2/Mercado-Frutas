    private void actualizarGrilla(Puesto unPuesto) throws NullPointerException {
        panelProductos.removeAll();
        ArrayList<Producto> lista = modelo.listaOrdenadaProductosDisponiblePorPuesto(unPuesto);
        for (Producto producto : lista) {
            JButton btnProducto = new JButton();
            btnProducto.setToolTipText(producto.getNombre());
            ImageIcon icono = new ImageIcon(producto.getImagen());
            btnProducto.setIcon(new ImageIcon(icono.getImage().
                    getScaledInstance(60, 60, java.awt.Image.SCALE_SMOOTH)));
            btnProducto.putClientProperty(producto.getNombre(), producto);
            btnProducto.addActionListener(new ProcesoProducto());
            panelProductos.add(btnProducto);
        }
        panelProductos.revalidate();
        panelProductos.repaint();
    }
    

    
