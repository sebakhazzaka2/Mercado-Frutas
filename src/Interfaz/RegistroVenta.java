    private class ProcesoProducto implements ActionListener {
        public void actionPerformed(ActionEvent e) {
            JButton cual = ((JButton) e.getSource());     
            Producto producto = (Producto)cual.getClientProperty(cual.getToolTipText());
            crearVentanaEmergente(producto,cual);
        }


        public void crearVentanaEmergente(Producto producto, JButton boton){
            JPanel panel = new JPanel(new GridLayout(0,1));
            JLabel lblPrecio = new JLabel ("Precio: ");
            JTextField txtPrecio = new JTextField();
            JLabel lblCantidad = new JLabel ("Cantidad: ");
            JTextField txtCantidad = new JTextField();
            JButton btnAceptar = new JButton("Aceptar");
            btnAceptar.addActionListener(new ActionListener(){
                public void actionPerformed(ActionEvent e) {
                    int cantidad = Integer.parseInt(txtCantidad.getText());
                    int precio = Integer.parseInt(txtPrecio.getText());
                    if (cantidad <= producto.getStock()) {
                        modelo.registrarVenta((Puesto)jComboBoxPuesto.getSelectedItem(),
                                producto, cantidad, precio);
                        JOptionPane.showMessageDialog(boton.getParent(),
                                "Venta realizada con Ã©xito",
                                "Venta", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(boton.getParent(),
                                "La cantidad supera el stock disponible.\n"+"El stock"
                                        + " disponible es de: "+producto.getStock(),
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            });
            panel.add(lblPrecio);
            panel.add(txtPrecio);
            panel.add(lblCantidad);
            panel.add(txtCantidad);
            panel.add(btnAceptar);
            JOptionPane.showOptionDialog(boton.getParent(), 
                    panel, 
                    producto.getNombre(), 
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.PLAIN_MESSAGE,
                    null,
                    new Object[]{},
                    null);
        }


    }

